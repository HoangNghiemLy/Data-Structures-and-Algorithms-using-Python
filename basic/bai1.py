#Viết lệnh xuất các biến sau:
# Kiểu dữ liệu số
x = 10
y = 10.5
z = 5
t1 = x+z
t2 = x+y
print("Kiểu dữ liệu số")
print(f"Giá trị của x là {x}")
print(f"Giá trị của y là {y}")
print(f"Giá trị của z là {z}")
print(f"Giá trị của t1 là {t1}")
print(f"Giá trị của t2 là {t2}")
print()

# Kiểu thực
a = 1.4
a1 = 31.4e-10
print("Kiểu dữ liệu thực")
print(f"Giá trị của a là {a}")
print(f"Giá trị của a1 là {a1}")
print()

#Kiểu số phức
z = 2 + 7j
print("Kiểu dữ liệu số phức")
print(f"Giá trị của z là {z}")
print()

#Kiểu số bát phân (8), thập lục phân (16) hoặc nhị phân (2)
